_Static_assert(sizeof(aes_gctr_linear_empty) == AES_FUN_SIZE, 
    "Definition of AES_FUN_SIZE does not align with the gctr function's size");

void init_counter_mode_function(aes_fun_code* const dest_code, aes_uint128* key_src) {
    aes_fun* const dest = (aes_fun*) dest_code;

    memcpy(dest_code, aes_gctr_linear_empty, sizeof(*dest_code));
    dest->str.key_lo.q = key_src->u64[0];
    dest->str.key_hi.q = key_src->u64[1];
    memset(key_src, 0, sizeof(*key_src));
}

