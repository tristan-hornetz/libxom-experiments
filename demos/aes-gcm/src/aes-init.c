typedef union {
    aes_fun_code buf;
    struct {
        uint8_t pad1_[4];
        union {
            uint8_t b[sizeof(uint64_t)];
            uint64_t q;
        } key_lo;
        uint8_t pad2_[7];
        union {
            uint8_t b[sizeof(uint64_t)];
            uint64_t q;
        } key_hi;
        uint8_t pad3_[AES_FUN_SIZE - 27];
    } __attribute__((packed)) str;
} aes_fun;

_Static_assert(sizeof(((aes_fun*)NULL)->buf) == sizeof(((aes_fun*)NULL)->str),
    "Definition of aes_fun does not align with the gctr function's size");

